
namespace calc {
    public class Calculator {
        public int add(int v1, int v2) {
            return v1 + v2;
            
        }
        public int subtract(int v1, int v2) {
            return v1 - v2;
        }
        public int multiply(int v1, int v2) {
            return v1 * v2;
        }
        public float divide(int v1, int v2) {
            return v1 / v2;
        }
        public int square(int v1) {
            return v1 * v1;
        }

    }
}
